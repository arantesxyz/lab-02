package xyz.arantes.edu.pmexercicio1.questao4;

public class Core {

    public static double converterPesParaMetros(double v){
        return v/3281;
    }

    public static double converterPesParaPolegadas(double v){
        return v*12;
    }

    public static double converterPesParaCentimetros(double v){
        return v*30.48;
    }

    public static double converterPolegadasParaMetros(double v){
        return v/39.37;
    }

    public static double converterPolegadasParaPes(double v){
        return v/12;
    }

    public static double converterPolegadasParaCentimetros(double v){
        return v*2.54;
    }
}
